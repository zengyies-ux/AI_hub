#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
测试提示词生成器的正则表达式修复
"""

import re

# 测试文本 - 模拟分析报告的格式
测试文本 = """
# 国际热点深度分析：中东战火、大国博弈与市场震荡

## 一、美伊冲突：从“定点清除”到全面对抗，中东火药桶全面引爆

**事件背景与起因**
本轮美伊冲突的导火索源于2月底美以联合对伊朗发动的大规模空袭。

## 二、美国内政外交的复杂互动

**内政与外交联动**
美国国内政治与外交政策相互影响，形成复杂的互动关系。

## 三、地缘政治冲击下的全球金融市场动荡

**市场反应**
中东冲突升级引发全球市场震荡，股市、原油、黄金等资产价格出现剧烈波动。

## 四、中国在国际变局中的角色与应对

**中国的立场与行动**
中国在国际变局中保持中立，积极推动和平解决争端。
"""

# 测试修复后的正则表达式
def 测试正则表达式():
    print("测试正则表达式修复...")
    print("=" * 60)
    
    行列表 = 测试文本.split('\n')
    话题列表 = []
    当前话题 = None
    当前内容 = []
    
    for 行 in 行列表:
        # 匹配 1. 标题 或 热点一： 或 一、标题 格式
        匹配 = re.match(r'^##\s*(?:(\d+)\.|热点[一二三四五六七八九十]+|[一二三四五六七八九十]+、)\s*[:：]?\s*(.+)$', 行)
        if 匹配:
            if 当前话题:
                话题列表.append({
                    '编号': 当前话题['编号'],
                    '标题': 当前话题['标题'],
                    '内容': '\n'.join(当前内容)
                })
            
            # 获取编号
            编号 = 匹配.group(1)
            # 如果是热点一：或一、标题格式，生成编号
            if not 编号:
                # 从行中提取中文数字并转换为阿拉伯数字
                中文数字匹配 = re.search(r'热点([一二三四五六七八九十]+)|([一二三四五六七八九十]+)、', 行)
                if 中文数字匹配:
                    中文数字 = 中文数字匹配.group(1) or 中文数字匹配.group(2)
                    数字映射 = {'一': '1', '二': '2', '三': '3', '四': '4', '五': '5', '六': '6', '七': '7', '八': '8', '九': '9', '十': '10'}
                    编号 = 数字映射.get(中文数字, '0')
                else:
                    编号 = str(len(话题列表) + 1)
            当前话题 = {
                '编号': 编号,
                '标题': 匹配.group(2).strip()
            }
            当前内容 = []
        elif 当前话题 and 行.strip():
            当前内容.append(行)
    
    # 添加最后一个话题
    if 当前话题:
        话题列表.append({
            '编号': 当前话题['编号'],
            '标题': 当前话题['标题'],
            '内容': '\n'.join(当前内容)
        })
    
    # 打印结果
    print(f"找到 {len(话题列表)} 个热点话题")
    print("-" * 60)
    for i, 话题 in enumerate(话题列表, 1):
        print(f"【{i}】编号: {话题['编号']}")
        print(f"   标题: {话题['标题']}")
        print(f"   内容: {话题['内容'][:50]}...")
        print()
    
    return 话题列表

if __name__ == "__main__":
    话题列表 = 测试正则表达式()
    if len(话题列表) >= 4:
        print("✅ 正则表达式修复成功！")
    else:
        print("❌ 正则表达式修复失败！")
